package com.naver.zootopia.model;

public class MarkerBean {
	private int		mark_no;
	private int		mark_type;
	private String	mark_name;
	private double	mark_lat;
	private double	mark_lng;
	private String	mark_address;
	private String	mark_tel;
	private String	mark_comment;
	
	
	public int getMark_no() {
		return mark_no;
	}
	public void setMark_no(int mark_no) {
		this.mark_no = mark_no;
	}
	public int getMark_type() {
		return mark_type;
	}
	public void setMark_type(int mark_type) {
		this.mark_type = mark_type;
	}
	public String getMark_name() {
		return mark_name;
	}
	public void setMark_name(String mark_name) {
		this.mark_name = mark_name;
	}
	public double getMark_lat() {
		return mark_lat;
	}
	public void setMark_lat(double mark_lat) {
		this.mark_lat = mark_lat;
	}
	public double getMark_lng() {
		return mark_lng;
	}
	public void setMark_lng(double mark_lng) {
		this.mark_lng = mark_lng;
	}
	public String getMark_address() {
		return mark_address;
	}
	public void setMark_address(String mark_address) {
		this.mark_address = mark_address;
	}
	public String getMark_tel() {
		return mark_tel;
	}
	public void setMark_tel(String mark_tel) {
		this.mark_tel = mark_tel;
	}
	public String getMark_comment() {
		return mark_comment;
	}
	public void setMark_comment(String mark_comment) {
		this.mark_comment = mark_comment;
	}
	
	
}
