package com.naver.zootopia.model;

public class ImageBean {

	private int image_num;
	private String join_id;
	private int board_num;
	private String board_original;
	private String board_DBfile;
	public int getImage_num() {
		return image_num;
	}
	public void setImage_num(int image_num) {
		this.image_num = image_num;
	}
	public String getJoin_id() {
		return join_id;
	}
	public void setJoin_id(String join_id) {
		this.join_id = join_id;
	}
	public int getBoard_num() {
		return board_num;
	}
	public void setBoard_num(int board_num) {
		this.board_num = board_num;
	}
	public String getBoard_original() {
		return board_original;
	}
	public void setBoard_original(String board_original) {
		this.board_original = board_original;
	}
	public String getBoard_DBfile() {
		return board_DBfile;
	}
	public void setBoard_DBfile(String board_DBfile) {
		this.board_DBfile = board_DBfile;
	}
	
}
