package com.naver.zootopia.model;

public class FavoriteBean {
	private int board_favorite_num;
	private String join_id;
	private int board_num;
	public int getBoard_favorite_num() {
		return board_favorite_num;
	}
	public void setBoard_favorite_num(int board_favorite_num) {
		this.board_favorite_num = board_favorite_num;
	}
	public String getJoin_id() {
		return join_id;
	}
	public void setJoin_id(String join_id) {
		this.join_id = join_id;
	}
	public int getBoard_num() {
		return board_num;
	}
	public void setBoard_num(int board_num) {
		this.board_num = board_num;
	}
	
}
